    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        if (num != "") {
            jTextDisplay.setText(num);
            System.out.println(num);
            ScriptEngineManager mgr = new ScriptEngineManager();
            ScriptEngine engine = mgr.getEngineByName("JavaScript");
            try {
                result = Double.parseDouble(engine.eval(num).toString());
            } catch (ScriptException ex) {
                Logger.getLogger(Basic_Calculator.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextDisplay.setText(String.valueOf(result));
            num = jTextDisplay.getText();
        }
    }//GEN-LAST:event_jButton16ActionPerformed

